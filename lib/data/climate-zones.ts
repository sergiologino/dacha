export function findClimateZone(lat: number, lon: number): ClimateZone | null {
  for (const zone of climateZones) {
    if (
      lat >= zone.latRange[0] && lat <= zone.latRange[1] &&
      lon >= zone.lonRange[0] && lon <= zone.lonRange[1]
    ) {
      return zone;
    }
  }
  return null;
}

