async function main() {
  for (const p of PROMPTS) {
    await prisma.prompt.upsert({
      where: { key: p.key },
      create: {
        key: p.key,
        prompt: p.prompt,
        description: p.description,
        endpoint: p.endpoint,
      },
      update: {
        prompt: p.prompt,
        description: p.description,
        endpoint: p.endpoint,
      },
    });
  }
  console.log("Seeded", PROMPTS.length, "prompts.");
}

main()
  .then(() => prisma.$disconnect())
  .catch((e) => {
    console.error(e);
    prisma.$disconnect();
    process.exit(1);
  });
